import math

print(math.ceil(23.5))  # округляет до большего

print(math.floor(23.5)) # округляет до меньшего

print(math.degrees(math.pi)) # передодит из радионов в углы

print(math.exp(2))  # эскпаненто

print(math.isqrt(9)) # выводит квадратный корень

print(math.factorial(5)) # факториал

print(math.sqrt(4)) # выводит кв конь с float

print(math.ulp(8)) # показывет значение бита

print(math.prod([1,2,3])) # умножает каждый элемент масива

print(math.lcm(2,8)) # общий знаменатель

print(math.gcd(6, 24)) # HOD


